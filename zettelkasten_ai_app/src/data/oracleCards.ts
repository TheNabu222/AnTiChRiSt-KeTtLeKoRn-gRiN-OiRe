export const allDecks: OracleDeck[] = [coaiexistDeck, anzuDeck];

export function getRandomCard(deckId: string): OracleCard | null {
  const deck = allDecks.find(d => d.id === deckId);
  if (!deck || deck.cards.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * deck.cards.length);
  return deck.cards[randomIndex];
}

export function getRandomCards(deckId: string, count: number): OracleCard[] {
  const deck = allDecks.find(d => d.id === deckId);
  if (!deck || deck.cards.length === 0) return [];
  
  const shuffled = [...deck.cards].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

export function getDeckById(deckId: string): OracleDeck | null {
  return allDecks.find(d => d.id === deckId) || null;
}
